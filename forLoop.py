"""
Loops
To perform single task multiple times (Repeat Tasks)
1) For Loop :
Syntax

for variable in range (x,y,z):
     #block of code

Where:
x is starting point
y is ending point (ending is y-1)
z is gap for iteration

"""

# Example 1
# to print from 1 to 10
for i in range (1,10): #10-1=9 is ending
    print(i)

#Example 2
# to print from hello world for 10 times
for i in range (1,11):
    print("Hello World",i)

# Example 3
# to print create a table of any number
for i in range (1,11):
    print(4,"*",i,"=",4*i)


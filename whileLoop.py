"""
Loops
To perform single task multiple times (Repeat Tasks)
2) while loop :

Syntax

i=0   #initialization

while condition:
     #block of code
     #increment

"""
# Example 1
i=1 #initialization
while i<11: #condition
    print(i)
    i+=1  #increment

# Example 2
i=1
while i<11:
    print("Hello World",i)
    i+=1

# Example 3
# to create a table
i=1
while i<11:
    print(45,"*",i,"=",45*i)
    i+=1
